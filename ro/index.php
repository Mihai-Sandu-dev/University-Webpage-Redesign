        </h4>

    <div class="semiTitle"><img src="../images/fingerprint-svgrepo-com.svg" alt=" " class="semiTitleIcon"><b> Rolul </b> Serviciului "Asigurarea Calității"? </div>
        <h4> 
            SAC oferă <b><b> suport metodologic și operațional </b></b> pentru toate structurile ULBS implicate în evaluarea și asigurarea calității. Echipa coordonează elaborarea documentației, organizează instruiri, monitorizează procesele interne și contribuie la evaluarea periodică a performanței universitare. Prin aceste acțiuni, SAC se asigură că standardele de calitate sunt respectate și îmbunătățite constant.
        </h4>

    <div class="semiTitle"><img src="../images/layers-svgrepo-com.svg" alt=" " class="semiTitleIcon"><b> Structura </b> Sistemului de Management al ULBS </div>
        <h4>
           Sistemul de Management al Calității la ULBS este format dintr-o <b><b>rețea de structuri interconectate: </b></b> Serviciul Asigurarea Calității, Comisia pentru Evaluarea și Asigurarea Calității la nivelul universității, Comisia Senatului privind programele de studii, precum și Subcomisiile SCEAC din fiecare facultate. Împreună, acestea contribuie la menținerea unui cadru unitar pentru monitorizarea și dezvoltarea calității în toate domeniile universitare.
        </h4>

