    <div class="semiTitle"><img src="../images/fingerprint-svgrepo-com.svg" alt=" " class="semiTitleIcon"> What is <b> the role of </b> the "Quality Assurance" Service (Q.A.S.)? </div>
        <h4> 
SAC provides <b><b>methodological and operational support</b></b> for all ULBS structures involved in quality assessment and assurance. The team coordinates the development of documentation, organizes training, monitors internal processes and contributes to the periodic evaluation of university performance. Through these actions, SAC ensures that quality standards are respected and constantly improved.        </h4>

    <div class="semiTitle"><img src="../images/layers-svgrepo-com.svg" alt=" " class="semiTitleIcon"> <b> The structure of </b> the "Quality Assurance" Service (Q.A.S.) </div>
        <h4>
The Quality Management System at ULBS is made up of a <b><b>network of interconnected structures: </b></b> the Quality Assurance Service, the University-wide Quality Assessment and Assurance Committee, the Senate Committee on Study Programs, as well as the SCEAC Subcommittees in each faculty. Together, they contribute to maintaining a unified framework for monitoring and developing quality in all university areas.        </h4>

